package bgu.spl.app.JSON;

public class Services {
	Time time;
	Manager manager;
	int factories;
	int sellers;
	Customer[] customers;
	
	public Time getTime() {
		return time;
	}
	public Manager getManager() {
		return manager;
	}
	public int getFactories() {
		return factories;
	}
	public int getSellers() {
		return sellers;
	}
	public Customer[] getCustomers() {
		return customers;
	}
	
}
